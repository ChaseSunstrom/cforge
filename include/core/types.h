/**
* @file   types.h
* @brief  Core types for the CForge library
*/

#ifndef CFORGE_TYPES_H
#define CFORGE_TYPES_H

typedef unsigned char cforge_byte_t; /**< Byte type */
typedef unsigned short cforge_ushort_t; /**< Word type */
typedef unsigned int cforge_uint_t; /**< Double word type */
typedef unsigned long long cforge_ulong_t; /**< Quad word type */
typedef signed char cforge_sbyte_t; /**< Signed byte type */
typedef signed short cforge_short_t; /**< Signed word type */
typedef signed int cforge_int_t; /**< Signed double word type */
typedef signed long long cforge_long_t; /**< Signed quad word type */
typedef float cforge_float_t; /**< Float type */
typedef double cforge_double_t; /**< Double type */
typedef long double cforge_ldouble_t; /**< Long double type */
typedef char cforge_char_t; /**< Character type */
typedef char* cforge_string_t; /**< String type */
typedef const char* cforge_cstring_t; /**< Constant string type */
typedef void* cforge_pointer_t; /**< Pointer type */
typedef size_t cforge_size_t; /**< Size type */

#endif