fs::path test_runner::write_builtin_header(const fs::path &gen_dir) {
  fs::path inc_dir = gen_dir / "cforge_includes";
  std::error_code ec;
  fs::create_directories(inc_dir, ec);

  fs::path header_path = inc_dir / "test_framework.h";
  std::ofstream out(header_path);
  if (!out) {
    logger::print_warning("Could not write generated header: " + header_path.string());
    return {};
  }
  out << get_builtin_test_framework_header();
  return inc_dir;
}

fs::path test_runner::generate_main_if_needed(const test_target &target,
                                              const fs::path &gen_dir) {
  // Only auto-generate for the cforge builtin framework. Other frameworks
  // (GTest's gtest_main, Catch2WithMain, doctest's _IMPLEMENT) handle this
  // themselves.
  if (target.framework != test_framework::Builtin &&
      target.framework != test_framework::Auto) {
    return {};
  }

  // Detect a user-defined main(...) in any source file. Use a regex that
  // ignores comments only loosely — false negatives just generate an extra
  // main and produce a clear linker error, which is fine.
  static const std::regex main_re(R"(\bint\s+main\s*\()");
  for (const auto &src : target.source_files) {
    std::string contents = read_file_to_string(src);
    if (std::regex_search(contents, main_re)) {
      return {}; // user has their own main
    }
  }

  fs::path main_path = gen_dir / "cforge_test_main.cpp";
  std::ofstream out(main_path);
  if (!out) {
    logger::print_warning("Could not write generated test main: " + main_path.string());
    return {};
  }
  out << "// Auto-generated by cforge — runs all auto-registered TEST() macros.\n"
         "#include \"test_framework.h\"\n"
         "int main(int argc, char** argv) { return cf_run_tests(argc, argv); }\n";
  return main_path;
}

bool test_runner::generate_test_cmake(const test_target &target) {
  // Use configured build directory for test outputs
  fs::path gen_dir = get_test_gen_dir(target.name);
  logger::print_verbose("Test CMake dir: " + gen_dir.string());
  fs::create_directories(gen_dir);

  // Drop the cforge builtin header into the build dir so users can
  // #include "test_framework.h" without ever copying a file into their source.
  fs::path generated_inc_dir = write_builtin_header(gen_dir);

  // Auto-generate a tiny main() if the user hasn't written one and the target
  // uses the builtin framework. Auto-registration via TEST() does the rest.
  fs::path generated_main = generate_main_if_needed(target, gen_dir);

  fs::path cmake_file = gen_dir / "CMakeLists.txt";
  std::ofstream out(cmake_file);
  if (!out) {
    m_error = "FAILED to create " + cmake_file.string();
    return false;
  }

  // Get adapter for framework
  auto *adapter = get_adapter(target.framework);
  if (!adapter) {
    m_error = "No adapter for framework: " + test_framework_to_string(target.framework);
    return false;
  }

  // Get framework config
  test_config::FrameworkConfig fw_config;
  auto it = m_test_config.framework_configs.find(target.framework);
  if (it != m_test_config.framework_configs.end()) {
    fw_config = it->second;
  }

  out << "cmake_minimum_required(VERSION 3.15)\n"
      << "project(" << target.name << "_test CXX C)\n\n"
      << "enable_testing()\n\n";

  // Set C++ standard from project config
  std::string cxx_std = m_project_config.get_string("project.cpp_standard", "17");
  out << "set(CMAKE_CXX_STANDARD " << cxx_std << ")\n"
      << "set(CMAKE_CXX_STANDARD_REQUIRED ON)\n\n";

  // Configure project dependencies via FetchContent (reads from cforge.toml)
  configure_index_dependencies_fetchcontent_phase1(m_project_dir, m_project_config, out);

  // Add framework setup
  std::string fw_setup = adapter->generate_cmake_setup(fw_config);
  if (!fw_setup.empty()) {
    out << fw_setup << "\n";
  }

  // Source files
  out << "# Test sources\n"
      << "set(TEST_SOURCES\n";
  for (const auto &src : target.source_files) {
    out << "  \"" << to_cmake_path(src) << "\"\n";
  }
  if (!generated_main.empty()) {
    out << "  \"" << to_cmake_path(generated_main) << "\"\n";
  }
  out << ")\n\n";

  // Create executable
  out << "add_executable(${PROJECT_NAME} ${TEST_SOURCES})\n\n";

  // Include directories
  out << "target_include_directories(${PROJECT_NAME} PRIVATE\n"
      << "  \"" << to_cmake_path(m_project_dir / "include") << "\"\n"
      << "  \"" << to_cmake_path(m_project_dir / "src") << "\"\n"
      << "  \"" << to_cmake_path(m_project_dir / m_test_config.directory) << "\"\n";
  if (!generated_inc_dir.empty()) {
    out << "  \"" << to_cmake_path(generated_inc_dir) << "\"\n";
  }
  for (const auto &inc : target.includes) {
    out << "  \"" << to_cmake_path(m_project_dir / inc) << "\"\n";
  }
  out << ")\n\n";

  // Link project dependencies (reads from cforge.toml)
  configure_index_dependencies_fetchcontent_phase2(m_project_dir, m_project_config, out);

  // Link framework and additional targets
  out << "target_link_libraries(${PROJECT_NAME} PRIVATE\n";

  // Framework target
  std::string cmake_target = adapter->get_cmake_target();
  if (!cmake_target.empty()) {
    out << "  " << cmake_target << "\n";
  }

  // Auto-link project if applicable
  if (should_auto_link_project()) {
    std::string project_target = get_project_link_target();
    if (!project_target.empty()) {
      out << "  " << project_target << "\n";
    }
  }

  // User dependencies
  for (const auto &dep : target.dependencies) {
    out << "  " << dep << "\n";
  }
  out << ")\n\n";

  // Defines
  if (!target.defines.empty()) {
    out << "target_compile_definitions(${PROJECT_NAME} PRIVATE\n";
    for (const auto &def : target.defines) {
      out << "  " << def << "\n";
    }
    out << ")\n\n";
  }

  // CTest integration — for every framework. GTest/Catch2 use their CMake
  // helpers for per-test discovery; for the others we register the binary as
  // a single test so plain `ctest` still works.
  if (target.framework == test_framework::GTest) {
    out << "include(GoogleTest)\n"
        << "gtest_discover_tests(${PROJECT_NAME})\n";
  } else if (target.framework == test_framework::Catch2) {
    out << "include(Catch)\n"
        << "catch_discover_tests(${PROJECT_NAME})\n";
  } else {
    out << "add_test(NAME " << target.name << " COMMAND ${PROJECT_NAME})\n";
    if (target.timeout_seconds > 0) {
      out << "set_tests_properties(" << target.name << " PROPERTIES TIMEOUT "
          << target.timeout_seconds << ")\n";
    }
  }

  return true;
}

bool test_runner::should_auto_link_project() const {
  if (!m_test_config.auto_link_project) {
    return false;
  }

  std::string type = m_project_config.get_string("project.binary_type", "executable");
  return (type == "library" || type == "static_library" ||
          type == "shared_library" || type == "static" || type == "shared");
}

std::string test_runner::get_project_link_target() const {
  std::string name = m_project_config.get_string("project.name", "");
  if (name.empty()) {
    return "";
  }
  return name;
}

