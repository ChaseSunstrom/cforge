bool test_runner::configure_cmake(const test_target &target,
                                  const std::string &build_config) {
  fs::path gen_dir = get_test_gen_dir(target.name);
  fs::path build_dir = get_test_build_dir(target.name);

  // Ensure build directory exists
  fs::create_directories(build_dir);

  std::vector<std::string> args = {
    "-S", to_cmake_path(gen_dir),
    "-B", to_cmake_path(build_dir),
    "-DCMAKE_BUILD_TYPE=" + build_config
  };

#ifdef _WIN32
  // Use same generator as main project if specified
  std::string generator = m_project_config.get_string("build.generator", "");
  if (!generator.empty()) {
    args.push_back("-G");
    args.push_back(generator);
  }
#endif

  // Use execute_process directly for cleaner error handling
  auto result = execute_process("cmake", args, m_project_dir.string(), nullptr, nullptr, 120);
  if (!result.success) {
    // Store error for later reporting
    m_error = "CMake configuration failed";
    logger::print_error("FAILED to configure " + target.name);
    if (!result.stderr_output.empty()) {
      logger::print_plain(result.stderr_output);
    }
    if (!result.stdout_output.empty()) {
      logger::print_plain(result.stdout_output);
    }
  }
  return result.success;
}

bool test_runner::build_target(const test_target &target,
                               const std::string &build_config) {
  fs::path build_dir = get_test_build_dir(target.name);

  std::vector<std::string> args = {
    "--build", to_cmake_path(build_dir),
    "--config", build_config
  };

  // Stream output live so compile errors are visible (cmake --build can exit 0
  // even when MSBuild reports errors — we also do a post-build exe check).
  auto result = execute_process(
      "cmake", args, m_project_dir.string(),
      [](const std::string &line) { logger::print_plain(line); },
      [](const std::string &line) { logger::print_error(line); },
      300);

  if (!result.success) {
    m_error = "Build failed";
    logger::print_error("FAILED to build " + target.name);
    return false;
  }

  // Detect the silent-failure case: cmake reports success but no executable
  // was produced. Treat as a build error rather than letting it surface later
  // as a confusing "executable not found".
  fs::path exe = find_test_executable(target, build_config);
  if (exe.empty()) {
    m_error = "Build reported success but no executable was produced for " +
              target.name + " (likely a compile error — see output above).";
    logger::print_error(m_error);
    return false;
  }
  return true;
}

bool test_runner::build_tests(const std::string &config, [[maybe_unused]] bool verbose) {
  // Discover targets if not already done
  if (m_test_config.targets.empty()) {
    discover_targets();
  }

  bool all_success = true;
  for (const auto &target : m_test_config.targets) {
    if (!target.enabled) continue;

    logger::print_action("Building", "test target: " + target.name);

    // Generate CMakeLists.txt
    if (!generate_test_cmake(target)) {
      logger::print_error("FAILED to generate CMake for " + target.name);
      all_success = false;
      continue;
    }

    // Configure (error printed inside)
    if (!configure_cmake(target, config)) {
      all_success = false;
      continue;
    }

    // Build (error printed inside)
    if (!build_target(target, config)) {
      all_success = false;
      continue;
    }
  }

  return all_success;
}

