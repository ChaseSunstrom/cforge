// Helper to convert path to CMake format (forward slashes)
static std::string to_cmake_path(const fs::path &path) {
  std::string str = path.string();
  std::replace(str.begin(), str.end(), '\\', '/');
  return str;
}

// Strip ANSI escape sequences so colored test output still parses cleanly.
static std::string strip_ansi(const std::string &input) {
  static const std::regex ansi_re("\x1b\\[[0-9;]*[A-Za-z]");
  return std::regex_replace(input, ansi_re, "");
}

// Read a file fully into a string. Empty string on failure.
static std::string read_file_to_string(const fs::path &path) {
  std::ifstream in(path);
  if (!in) return {};
  std::ostringstream oss;
  oss << in.rdbuf();
  return oss.str();
}

// Bounded recursive search for an executable named like the target. Walks the
// build dir up to a moderate depth so it works with single- and multi-config
// generators (Ninja vs MSBuild) without hardcoded paths.
static fs::path find_executable_in_dir(const fs::path &build_dir,
                                       const std::vector<std::string> &name_candidates) {
  if (!fs::exists(build_dir)) return {};

  std::error_code ec;
  for (auto it = fs::recursive_directory_iterator(
                  build_dir, fs::directory_options::skip_permission_denied, ec);
       !ec && it != fs::recursive_directory_iterator(); it.increment(ec)) {
    if (it.depth() > 6) {
      it.disable_recursion_pending();
      continue;
    }
    if (!it->is_regular_file(ec)) continue;
    const auto &p = it->path();
    auto stem = p.stem().string();
    auto filename = p.filename().string();
    auto ext = p.extension().string();

#ifdef _WIN32
    // Only consider .exe on Windows
    if (ext != ".exe") continue;
#else
    // On POSIX, accept extension-less files; skip generated CMake junk
    if (!ext.empty() && ext != ".out") continue;
    if (filename.find("CMake") != std::string::npos) continue;
#endif

    for (const auto &cand : name_candidates) {
      if (stem == cand || filename == cand) {
        return p;
      }
    }
  }
  return {};
}

